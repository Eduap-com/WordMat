      subroutine graeff(a,b)
c
c     --  Graeffe Root-Squaring Method   --
c     --  to Find Roots of a Polynomial  --
c
      b(1)=a(11)**2
      b(2)=2.0*a(9)*a(11)-a(10)**2
      b(3)=2.0*a(7)*a(11)-2.0*a(8)*a(10)+a(9)**2
      b(4)=2.0*a(5)*a(11)-2.0*a(6)*a(10)+2.0*a(7)*a(9)-a(8)**2
      b(5)=2.0*a(3)*a(11)-2.0*a(4)*a(10)+2.0*a(5)*a(9)-2.0*a(6)*a(8)+a(7
     . )**2
      b(6)=2.0*a(1)*a(11)-2.0*a(2)*a(10)+2.0*a(3)*a(9)-2.0*a(4)*a(8)+2.0
     . *a(5)*a(7)-a(6)**2
      b(7)=2.0*a(1)*a(9)-2.0*a(2)*a(8)+2.0*a(3)*a(7)-2.0*a(4)*a(6)+a(5)
     . **2
      b(8)=2.0*a(1)*a(7)-2.0*a(2)*a(6)+2.0*a(3)*a(5)-a(4)**2
      b(9)=2.0*a(1)*a(5)-2.0*a(2)*a(4)+a(3)**2
      b(10)=2.0*a(1)*a(3)-a(2)**2
      b(11)=a(1)**2
      return
      end
